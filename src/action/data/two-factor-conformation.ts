import PRISMA from "@/utils/prisma";

export const getTwoFactorConformationByUserId = async (userId: string) => {
  try {
    const twoFactorConformation = await PRISMA.twoFactorConfirmation.findUnique(
      {
        where: { userId },
      }
    );

    return twoFactorConformation;
  } catch {
    return null;
  }
};
